"""Tests for src/nlhe/archetypes.py.

Behavioral tests rather than unit tests: each test verifies that the
archetype produces sensible action distributions on canonical scenarios,
using the real bucket-equity calibration from
runs/archetype_design/bucket_equity_analysis.json.

If the calibration JSON is missing the tests skip rather than fail --
the calibration is regenerated by scripts/analyze_bucket_equity.py.
"""
import random
from pathlib import Path

import numpy as np
import pytest

from src.nlhe.actions import DiscreteAction
from src.nlhe.archetypes import (
    NAMED_ARCHETYPES,
    ArchetypeName,
    EquityCalibration,
    OpponentPool,
    archetype_policy,
    derive_in_position,
)

CALIBRATION_PATH = Path("runs/archetype_design/bucket_equity_analysis.json")
N_DISCRETE_ACTIONS = 7  # FOLD, CALL, BET_33, BET_66, BET_100, BET_200, ALLIN


@pytest.fixture(scope="module")
def calibration():
    if not CALIBRATION_PATH.exists():
        pytest.skip(f"calibration not found at {CALIBRATION_PATH}")
    return EquityCalibration.load(CALIBRATION_PATH)


@pytest.fixture(scope="module")
def all_legal_mask():
    return np.ones(N_DISCRETE_ACTIONS, dtype=np.float32)


@pytest.fixture(scope="module")
def no_bet_mask():
    """Legal mask for "no bet to call" situation: FOLD is illegal, CALL=check
    is legal, all bets legal."""
    m = np.ones(N_DISCRETE_ACTIONS, dtype=np.float32)
    m[int(DiscreteAction.FOLD)] = 0.0
    return m


def _best_bucket(calibration, street):
    """Return the bucket ID with the highest equity on this street."""
    eqs = calibration.bucket_equity[street]
    return int(np.argmax(eqs))


def _worst_bucket(calibration, street):
    """Return the bucket ID with the lowest equity on this street."""
    eqs = calibration.bucket_equity[street]
    return int(np.argmin(eqs))


def _by_name(name):
    """Find the archetype profile by name."""
    for a in NAMED_ARCHETYPES:
        if a.name == name:
            return a
    raise KeyError(name)


# ----- Test 1: archetypes produce valid distributions -----

def test_policy_returns_valid_distribution(calibration, all_legal_mask):
    """Every archetype, on a representative scenario, returns a valid
    probability distribution over legal actions."""
    rng = random.Random(0)
    for arch in NAMED_ARCHETYPES:
        dist = archetype_policy(
            archetype=arch,
            calibration=calibration,
            street_idx=0,
            bucket_id=10,  # middle bucket
            in_position=True,
            pot_odds=0.33,
            stack_to_pot=20.0,  # deep
            legal_mask=all_legal_mask,
            facing_bet=True,
            rng=rng,
        )
        assert dist.shape == (N_DISCRETE_ACTIONS,)
        assert np.all(dist >= 0), f"{arch.name.name}: negative weights {dist}"
        assert np.isclose(dist.sum(), 1.0), \
            f"{arch.name.name}: distribution sums to {dist.sum()}, not 1.0"


# ----- Test 2: nit folds trash preflop, plays premiums -----

def test_nit_folds_trash_preflop(calibration, all_legal_mask):
    """Nit facing a preflop bet on the worst bucket should fold with very high probability."""
    nit = _by_name(ArchetypeName.NIT)
    worst = _worst_bucket(calibration, "preflop")
    rng = random.Random(0)
    dist = archetype_policy(
        archetype=nit, calibration=calibration,
        street_idx=0, bucket_id=worst,
        in_position=False, pot_odds=0.33, stack_to_pot=20.0,
        legal_mask=all_legal_mask, facing_bet=True, rng=rng,
    )
    assert dist[int(DiscreteAction.FOLD)] > 0.9, \
        f"nit on worst preflop bucket: fold prob {dist[int(DiscreteAction.FOLD)]:.3f}, " \
        f"expected > 0.9. Full dist: {dist}"


def test_nit_plays_premium_preflop(calibration, all_legal_mask):
    """Nit on the best preflop bucket should not fold to a preflop bet."""
    nit = _by_name(ArchetypeName.NIT)
    best = _best_bucket(calibration, "preflop")
    rng = random.Random(0)
    dist = archetype_policy(
        archetype=nit, calibration=calibration,
        street_idx=0, bucket_id=best,
        in_position=False, pot_odds=0.33, stack_to_pot=20.0,
        legal_mask=all_legal_mask, facing_bet=True, rng=rng,
    )
    assert dist[int(DiscreteAction.FOLD)] < 0.1, \
        f"nit on best preflop bucket: fold prob {dist[int(DiscreteAction.FOLD)]:.3f}, " \
        f"expected < 0.1. Full dist: {dist}"


# ----- Test 3: maniac plays trash, doesn't fold premiums -----

